package zap

import (
	"testing"

	"go.uber.org/zap/testutils"
	"go.uber.org/zap/zapcore"
)

func withBenchedSugar(b *testing.B, f func(*SugaredLogger)) {
	logger := New(zapcore.WriterFacility(zapcore.NewJSONEncoder(defaultEncoderConfig()),
		&testutils.Discarder{},
		DebugLevel,
	)).Sugar()
	b.ResetTimer()
	b.RunParallel(func(pb *testing.PB) {
		for pb.Next() {
			f(logger)
		}
	})
}

func Benchmark10FieldsSugar(b *testing.B) {
	withBenchedSugar(b, func(logger *SugaredLogger) {
		logger.Infow("Ten fields, passed at the log site.",
			"one", 1,
			"two", 2,
			"three", 3,
			"four", 4,
			"five", 5,
			"six", 6,
			"seven", 7,
			"eight", 8,
			"nine", 9,
			"ten", 10,
		)
	})
}
